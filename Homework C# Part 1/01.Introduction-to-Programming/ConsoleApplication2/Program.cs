using System;

class SquareOfANumber
{
    static void Main()
    {
        int number = 12345;
        int squareNumber = number * number;
        Double squareRootNumber = Math.Sqrt(number);
        Console.WriteLine("Square of te Number {0} is: {1}", number, squareNumber);
        Console.WriteLine("Square root of te Number {0} is: {1}", number, squareRootNumber);
    }
}

